//Crie um script que declare 2 variaveis e exiba o resultado da soma entre elas.

let number1 = prompt("Digite um número?")
let number2 = prompt("Digite outro número?")
let resultado = ''

resultado = Number(number1) +  Number(number2)
alert(`A soma dos dois números é ${resultado}`)

resultado = Number(number1) -  Number(number2)
alert(`A subtração dos dois números é ${resultado}`)

resultado = Number(number1) *  Number(number2)
alert(`A multiplicação dos dois números é ${resultado}`)

resultado = Number(number1) /  Number(number2)
alert(`A divisão dos dois números é ${resultado}`)

